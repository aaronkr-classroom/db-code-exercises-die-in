CREATE TABLE 사원 (
    사원번호 INT PRIMARY KEY,
    이름 VARCHAR(20) NOT NULL,
    입사일 DATE NOT NULL,
    호봉 INT NOT NULL DEFAULT 10,
    휴대폰 VARCHAR(13)
);

